class Command(BaseCommand):
    help = 'Popula o banco com os 100 clientes do professor'

    def handle(self, *args, **kwargs):
        Cliente.objects.all().delete()
        criados = 0
        for row in DADOS:
            _, nome, cpf, dias, gasto, categoria = row
            c = Cliente(nome=nome, cpf=cpf, dias_sem_comprar=dias,
                        total_gasto_mes=gasto, categoria_mais_comprada=categoria)
            c.classificar()
            c.save()
            criados += 1
        self.stdout.write(self.style.SUCCESS(f'✅ {criados} clientes criados e classificados!'))
